async function createBleData(cmd, data) {
  await writeDataToBLE(new Uint8Array([cmd, 0x00, 0x00, 0x00, 0x00]).buffer);
  //await delay(1000);
  // 然后给data分段，180字节一个段
  var index = 1;
  var dataLength = data.length;
  var len = 180;
  while (len == 180) {
    if (index * 180 + 180 > dataLength) {
      len = dataLength - index * 180;
    }
    var u8array = new Uint8Array(len);
    for (var i = 0; i < len; i++) {
      u8array[i] = data[index * 180 + i];
    }
    var u8arrayToSend = new Uint8Array(len + 4);
    u8arrayToSend[0] = cmd;
    u8arrayToSend[1] = index;
    u8arrayToSend[2] = len;
    u8arrayToSend[len + 3] = getCRC(u8array);
    console.log('crc=', u8arrayToSend[len + 3])
    for (var i = 0; i < len; i++) {
      u8arrayToSend[i + 3] = u8array[i];
    }

    await writeDataToBLE(u8arrayToSend.buffer);
    index++;
    //await delay(1000);
  }
  writeDataToBLE(new Uint8Array([cmd, 0xff, 0xff, 0xff, 0xff]).buffer);
}

function getCRC(data) {
  var crc = 0;
  data.forEach(item => {
    crc += item;
    if (crc > 256) {
      crc -= 256;
    }
  });
  return crc;
}

